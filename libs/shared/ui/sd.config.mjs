// ─── Format: radii ───────────────────────────────────────────────────────────

StyleDictionary.registerFormat({
  name: 'rn/radii',
  format: ({ dictionary }) => {
    const tokens = dictionary.allTokens.filter(
      (t) => t.path[1] === 'borderRadius',
    );

    const entries = tokens
      .map((t) => {
        const comment = t.description ? ` // ${t.description}` : '';
        return `  ${t.path[2]}: ${Number(t.value)},${comment}`;
      })
      .join('\n');

    return [
      header,
      `export const radii = {`,
      entries,
      `} as const;`,
      ``,
      `export type RadiusToken = keyof typeof radii;`,
      ``,
    ].join('\n');
  },
});

// ─── Format: typography ──────────────────────────────────────────────────────

StyleDictionary.registerFormat({
  name: 'rn/typography',
  format: ({ dictionary }) => {
    // Group composite tokens: path could be depth-3 (token itself) or depth-4 (expanded prop).
    // Tokens Studio keeps typography as a single composite token (depth 3).
    const tokens = dictionary.allTokens.filter(
      (t) => t.path[1] === 'typography' && t.path.length === 3,
    );

    const entries = tokens
      .map((t) => {
        const v = t.value;
        const props = [];
        if (v.fontFamily) props.push(`    fontFamily: '${v.fontFamily}',`);
        if (v.fontWeight) props.push(`    fontWeight: '${v.fontWeight}' as const,`);
        if (v.fontSize) props.push(`    fontSize: ${Number(v.fontSize)},`);
        if (v.lineHeight) props.push(`    lineHeight: ${Number(v.lineHeight)},`);
        if (v.letterSpacing) props.push(`    letterSpacing: ${Number(v.letterSpacing)},`);

        const comment = t.description ? `  // ${t.description}\n` : '';
        return `${comment}  ${t.path[2]}: {\n${props.join('\n')}\n  } satisfies TextStyle,`;
      })
      .join('\n\n');

    return [
      header,
      `import { TextStyle } from 'react-native';`,
      ``,
      `export const typography = {`,
      entries,
      `} as const;`,
      ``,
      `export type TypographyToken = keyof typeof typography;`,
      ``,
    ].join('\n');
  },
});

// ─── Format: shadows ─────────────────────────────────────────────────────────
// React Native only supports a single shadow layer.
// We use the outermost (last) layer from Figma's multi-shadow as the base.

StyleDictionary.registerFormat({
  name: 'rn/shadows',
  format: ({ dictionary }) => {
    const tokens = dictionary.allTokens.filter(
      (t) => t.path[1] === 'boxShadow' && t.path.length === 3,
    );

    const entries = tokens
      .map((t) => {
        const layers = Array.isArray(t.value) ? t.value : [t.value];
        // Use the outermost (largest) shadow layer as the RN base
        const outer = layers[layers.length - 1];

        const shadowColor = outer.color.startsWith('rgba')
          ? rgbaToHex(outer.color)
          : outer.color;
        const opacity = rgbaAlpha(outer.color);
        const blur = Number(outer.blur);
        const y = Math.abs(Number(outer.y));

        const comment = t.description ? `  // ${t.description}\n` : '';
        return [
          `${comment}  ${t.path[2]}: {`,
          `    shadowColor: '${shadowColor}',`,
          `    shadowOffset: { width: 0, height: ${y} },`,
          `    shadowOpacity: ${opacity},`,
          `    shadowRadius: ${blur},`,
          `    elevation: ${Math.round(blur * 0.5)},`,
          `  } satisfies ViewStyle,`,
        ].join('\n');
      })
      .join('\n\n');

    return [
      header,
      `import { ViewStyle } from 'react-native';`,
      ``,
      `// React Native only supports a single shadow layer natively.`,
      `// Values derived from the outermost Figma effect layer.`,
      `export const shadows = {`,
      entries,
      `} as const;`,
      ``,
      `export type ShadowToken = keyof typeof shadows;`,
      ``,
    ].join('\n');
  },
});

// ─── Build ───────────────────────────────────────────────────────────────────

const sd = new StyleDictionary({
  source: ['libs/shared/ui/src/tokens/tokens.json'],
  // Collisions are expected — each formatter intentionally uses only a subset of tokens.
  log: { warnings: 'disabled' },
  platforms: {
    rn: {
      buildPath: 'libs/shared/ui/src/tokens/',
      files: [
        { destination: 'colors.ts', format: 'rn/colors' },
        { destination: 'spacing.ts', format: 'rn/spacing' },
        { destination: 'radii.ts', format: 'rn/radii' },
        { destination: 'typography.ts', format: 'rn/typography' },
        { destination: 'shadows.ts', format: 'rn/shadows' },
      ],
    },
  },
});

await sd.buildAllPlatforms();
console.log('\n✅ Tokens built successfully.\n');
