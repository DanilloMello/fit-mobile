import StyleDictionary from 'style-dictionary';

// ─── Helpers ─────────────────────────────────────────────────────────────────

/** Extract hex color from rgba(r,g,b,a) string */
function rgbaToHex(color) {
  const match = color.match(/rgba?\((\d+),\s*(\d+),\s*(\d+)/);
  if (!match) return color;
  const [, r, g, b] = match;
  return `#${Number(r).toString(16).padStart(2, '0')}${Number(g).toString(16).padStart(2, '0')}${Number(b).toString(16).padStart(2, '0')}`.toUpperCase();
}

/** Extract alpha channel from rgba(r,g,b,a) string */
function rgbaAlpha(color) {
  const match = color.match(/rgba\([^,]+,[^,]+,[^,]+,\s*([^)]+)\)/);
  return match ? parseFloat(match[1]) : 1;
}

/** Auto-generated file header */
const header = `// This file is auto-generated by Style Dictionary.
// Edit libs/shared/ui/src/tokens/tokens.json (via Tokens Studio) — do not edit directly.
`;

// ─── Format: colors ──────────────────────────────────────────────────────────

StyleDictionary.registerFormat({
  name: 'rn/colors',
  format: ({ dictionary }) => {
    const tokens = dictionary.allTokens.filter(
      (t) => t.path[1] === 'colors',
    );

    const entries = tokens
      .map((t) => `  ${t.path[2]}: '${t.value}',`)
      .join('\n');

    return [
      header,
      `export const colors = {`,
      entries,
      `} as const;`,
      ``,
      `export type ColorToken = keyof typeof colors;`,
      ``,
    ].join('\n');
  },
});

// ─── Format: spacing ─────────────────────────────────────────────────────────

